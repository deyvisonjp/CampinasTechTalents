public class TiposPrimitivosFloatEDouble {

    float a = 10.99f;

    double b = 10.3D;

    double c = 5;

    double d = 7;

    @Override
    public String toString() {
        return "Doubles e Floats" + "\n" +
                "Valor explícito float ... a = " + a + "\n" +
                "Valor explícito double ... b = " + b + "\n" +
                "Valor inteiro em um double ... c = " + c + "\n" +
                "Valor double padrão ... d = " + d + "\n" +
                '}';
    }
}
