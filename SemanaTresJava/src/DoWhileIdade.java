public class DoWhileIdade {
    public static void main(String[]args) {
        int idade = 15;
        do {
            System.out.println("Sua idade atual é de: " + idade);
            idade++;
        } while (idade <= 18);
        System.out.println("Agora você tem 18 anos, se prepare para pagar muitos boletos!");
    }
}
